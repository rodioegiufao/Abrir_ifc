    // üî• FUN√á√ÉO MELHORADA PARA SINCRONIZAR C√ÇMERAS
    function syncCamerasToXeokit() {
        if (!viewer || !xeokitViewer || !xeokitViewer.camera) {
            console.warn("‚ö†Ô∏è Sincroniza√ß√£o: viewer ou xeokit n√£o dispon√≠vel");
            return;
        }
        
        try {
            // ‚úÖ CORRE√á√ÉO: Verifica se a estrutura da c√¢mera existe
            const scene = viewer.context.getScene();
            if (!scene || !scene.camera) {
                console.warn("‚ö†Ô∏è Sincroniza√ß√£o: c√¢mera do Three.js n√£o dispon√≠vel");
                return;
            }

            const threeCamera = scene.camera;
            const threeControls = viewer.context.ifcCamera?.controls;
            
            if (!threeCamera || !threeControls) {
                console.warn("‚ö†Ô∏è Sincroniza√ß√£o: c√¢mera ou controles n√£o encontrados");
                return;
            }

            const threePos = threeCamera.position;
            const threeTarget = threeControls.target;

            if (!threePos || !threeTarget) {
                console.warn("‚ö†Ô∏è Sincroniza√ß√£o: posi√ß√£o ou target inv√°lidos");
                return;
            }

            // Sincroniza posi√ß√£o e lookAt
            xeokitViewer.camera.eye = [threePos.x, threePos.y, threePos.z];
            xeokitViewer.camera.look = [threeTarget.x, threeTarget.y, threeTarget.z];
            
            // Sincroniza FOV
            if (threeCamera.fov) {
                xeokitViewer.camera.perspective.fov = threeCamera.fov;
            }
            
            console.log("üì∏ C√¢mera sincronizada:", {
                eye: xeokitViewer.camera.eye,
                look: xeokitViewer.camera.look,
                fov: xeokitViewer.camera.perspective.fov
            });
            
        } catch (syncError) {
            console.warn("‚ö†Ô∏è Erro na sincroniza√ß√£o de c√¢mera:", syncError);
        }
    }

